answer = int(input())
guess = "nil"
takes = 0
while guess != answer:
  guess = int(input())
  if guess > answer and guess != answer:
    print("Guess was too high")
  elif guess != answer:
    print("Guess was too low")
  takes = takes+1
print("Congratulations - you guessed the number!")
print("Guesses Taken:",takes)